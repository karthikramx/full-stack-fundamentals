// Switch Statement

const superHero = 'Iron Man'

switch (superHero) {
    case 'Iron Man':
        console.log('Iam Iron Man')
        console.log('this is a test')
        break;
    case 'Capital America':
        console.log('I can do this all day')
        break;
    case 'Thor':
        console.log('No one can lift my hammer')
        break;
    default:
        console.log("Go get a super power first!")
        break;
}

