// Arithematics Operators Demo in Javascript 

// Sum of two numbers
const sum = 10 + 1;
console.log(sum)

// Difference of two numbers
const diff = 10 - 9;
console.log(diff)

// Division of two numbers
const div = 10 / 3;
console.log(div)

// Multiplication of two numbers
const mult = 10 * 3.33;
console.log(mult)

// Power 
const pow = 10 ** 2;
console.log(pow)

// Modulus
const mod = 10 % 3;
console.log(mod)

// Increment Operator 
let inc = 10
inc++;
console.log(inc)

// Decrement Operator 
let dec = 11;
dec--;
console.log(dec)