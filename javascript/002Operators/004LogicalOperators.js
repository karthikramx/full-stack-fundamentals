// Logical Operators
// || , &&, ! 

// && Operator 
console.log(true && true)
console.log(true && true && true) // can chain conditions like this... 

// OR Operator 
console.log(true || true);
console.log(true || false)
console.log(true || true || false)

// ! Not Operator 
console.log(!true)
console.log(!false)




